void **ft_print(char **map)
{
	while (*map + i)
	{
		while (*(map[i] + j) != '\n')
		{
			write(1, map[i] + j, 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
	write(1, "\n", 1);
}
